CREATE TABLE IF NOT EXISTS T_PRODUCTOS(
    ID_PRODUCTO INT (11) AUTO_INCREMENT NOT NULL,
    NOMBRE_PRODUCTO VARCHAR (100) NOT NULL,
    PRECIO FLOAT (100,2) NOT NULL,
    STOCK INT(11) NOT NULL,
    OFERTA VARCHAR (50) NOT NULL,
    FECHA DATE,
