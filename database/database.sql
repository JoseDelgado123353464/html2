    ID_CATEGORIA INT (11) NOT NULL,
    CONSTRAINT PK_PRODUCTO PRIMARY KEY (ID_PRODUCTO),
    CONSTRAINT FK_PRODUCTO_CATEGORIA FOREIGN KEY (ID_CATEGORIA) REFERENCES T_CATEGORIAS (ID_CATEGORIA)
)ENGINE=INNODB;

